def anim_text(text):
        
        for i in text:
            sys.stdout.write(i)  
            sys.stdout.flush()
            time.sleep(0.05)


def formating_news(namenews,name,date,description,link):
    
    post = []
    text = namenews+'|'
   
    for na,d,ds,l in zip(name[::-1],date[::-1],description[::-1],link[::-1]):
        post.append(f" {na} \n_________________________________\n\n{d}\n_________________________________\n{ds} \n\n {l} \n\n\n_________________________________\n")    
    
    return text.join(post)
